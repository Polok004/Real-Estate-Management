    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
        // TODO add your handling code here:
       String fname=jTextField_first.getText();
       String lname=jTextField_last.getText();
       String phone=jTextField_phone.getText();
       String email=jTextField_user.getText();
       String address=jTextArea_address.getText();
       String password=String.valueOf(jPasswordField1.getPassword());
       P_REGISTER owner=new P_REGISTER();
        try{
            
            int ownerId=Integer.valueOf(jTextField_id.getText());
            
            if(fname.trim().equals("") || lname.trim().equals("")||phone.trim().equals("")||email.trim().equals("")||address.trim().equals("")||password.trim().equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter The Agent Information First ","Edit Agent",1);
            }
        else{
               if(password.equals(new P_REGISTER().getPasswordById(ownerId))){
            if(owner.editAgentData(new P_REGISTER(ownerId,fname,lname,email,password,phone,address)))
            {
                JOptionPane.showMessageDialog(null,"New Agent Data Edited To The System","Edit Agent",1);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Agent Data NOT Edited To The System","Edit Agent",2);
            }
       
            }
               else
            {
                    JOptionPane.showMessageDialog(null,"Incorrect Password","Edit Agent Data",2);
           
             }
            }
            
        }catch(Exception ex)
                    {
                        JOptionPane.showMessageDialog(null,"Enter Valid Data","Edit Owner",2);
                    }
                   
        
    }//GEN-LAST:event_jButton_editActionPerformed

