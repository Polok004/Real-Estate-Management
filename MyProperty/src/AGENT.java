     public void fillJtableWithOwnerData() throws SQLException
    {
        P_REGISTER agent=new P_REGISTER();
        ArrayList<P_REGISTER> ownerList=agent.agentList();
        String[] colNames={"ID","First Name","Last Name","Username","Phone","Address"};
        Object[][] rows=new Object[ownerList.size()][6];
        for(int i=0;i<ownerList.size();i++)
        {
            rows[i][0]=ownerList.get(i).getId();
            rows[i][1]=ownerList.get(i).getFirstname();
            rows[i][2]=ownerList.get(i).getLastname();
            rows[i][4]=ownerList.get(i).getPhone();
            rows[i][3]=ownerList.get(i).getEmail();
            rows[i][5]=ownerList.get(i).getAddress();
            
        }
        DefaultTableModel model=new DefaultTableModel(rows,colNames);
        jTable_agent.setModel(model);
        jTable_agent.setRowHeight(40);
        jTable_agent.setSelectionBackground(Color.black);
        jTable_agent.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTable_agent.getColumnModel().getColumn(5).setPreferredWidth(150);
        jTable_agent.getColumnModel().getColumn(3).setPreferredWidth(150);
    }
